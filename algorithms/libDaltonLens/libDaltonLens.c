void dl_simulate_cvd_machado2009 (enum DLDeficiency deficiency, float severity, unsigned char* srgba_image, size_t width, size_t height, size_t bytesPerRow)
{
    if (!dl_validate_inputs(srgba_image, width, height, severity)) return;

    /* Select the matrix table for this deficiency. */
    const float (*table)[9];
    switch (deficiency)
    {
        case DLDeficiency_Protan:  table = dl_machado_protan;  break;
        case DLDeficiency_Deutan:  table = dl_machado_deutan;  break;
        case DLDeficiency_Tritan:  table = dl_machado_tritan;  break;
        default: return; /* ACHROMAT and BCM are not supported */
    }

    /* Interpolate between the two nearest precomputed matrices.
       alpha matches the Python formula: severity - lower*0.1 in range [0, 0.1].
       (Python: alpha = severity - severity_lower/10.0; NOT normalised to [0,1].) */
    int lower = (int)(severity * 10.0f);
    if (lower < 0)  lower = 0;
    if (lower > 10) lower = 10;
    int upper = (lower < 10) ? lower + 1 : 10;
    float alpha = severity - (float)lower * 0.1f;

    float m[9];
    for (int i = 0; i < 9; ++i)
        m[i] = (1.0f - alpha) * table[lower][i] + alpha * table[upper][i];

    if (bytesPerRow == 0) bytesPerRow = width * 4;

    for (size_t row = 0; row < height; ++row)
    {
        unsigned char* p = srgba_image + bytesPerRow * row;
        for (size_t col = 0; col < width * 4; col += 4)
        {
            const float r = linearRGB_from_sRGB(p[col + 0]);
            const float g = linearRGB_from_sRGB(p[col + 1]);
            const float b = linearRGB_from_sRGB(p[col + 2]);
            const float rc = m[0]*r + m[1]*g + m[2]*b;
            const float gc = m[3]*r + m[4]*g + m[5]*b;
            const float bc = m[6]*r + m[7]*g + m[8]*b;
            p[col + 0] = sRGB_from_linearRGB(rc);
            p[col + 1] = sRGB_from_linearRGB(gc);
            p[col + 2] = sRGB_from_linearRGB(bc);
        }
    }
}

void dl_simulate_cvd (enum DLDeficiency deficiency, float severity, unsigned char* srgba_image, size_t width, size_t height, size_t bytesPerRow)
{
    if (!dl_validate_inputs(srgba_image, width, height, severity)) return;

    switch (deficiency)
    {
        case DLDeficiency_Tritan:
            /* Viénot 1999 is not accurate for tritanopia; use Brettel 1997. */
            dl_simulate_cvd_brettel1997(deficiency, severity, srgba_image, width, height, bytesPerRow);
            break;
        case DLDeficiency_Achromat:
            dl_simulate_cvd_achromat(severity, srgba_image, width, height, bytesPerRow);
            break;
        case DLDeficiency_BCM:
            dl_simulate_cvd_bcm(severity, srgba_image, width, height, bytesPerRow);
            break;
        case DLDeficiency_Protan:
        case DLDeficiency_Deutan:
        default:
            /* Use Machado 2009 for anomalous trichromacy (severity < 1); Vienot
               at full severity matches Simulator_AutoSelect in DaltonLens-Python. */
            if (severity >= 0.9999f)
                dl_simulate_cvd_vienot1999(deficiency, severity, srgba_image, width, height, bytesPerRow);
            else
                dl_simulate_cvd_machado2009(deficiency, severity, srgba_image, width, height, bytesPerRow);
            break;
    }
}


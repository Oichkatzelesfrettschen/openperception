/* Guard against invalid inputs to avoid silent garbage output or segfaults.
   Returns 1 if inputs are valid, 0 if the call should be a no-op. */
static int dl_validate_inputs(const unsigned char *srgba_image, size_t width, size_t height, float severity)
{
    if (srgba_image == NULL) return 0;
    if (width == 0 || height == 0) return 0;
    if (severity < 0.0f) return 0;
    if (severity > 1.0f) return 0;
    return 1;
}


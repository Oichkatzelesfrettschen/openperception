static inline float linearRGB_from_sRGB(unsigned char v)
{
    return dl_srgb_to_linear_lut[v];
}

/* sRGB encoding via inverse LUT: binary-search the forward LUT for nearest value.
   This is intentionally simple; for a production path a separate inverse LUT
   or the powf path below would be faster. */
static inline unsigned char sRGB_from_linearRGB(float v)
{
    if (v <= 0.f) return 0;
    if (v >= 1.f) return 255;
    /* Linear search from the expected range center is fast enough for 256 entries
       when the LUT fits in L1 cache. Replace with binary search if profiling shows
       this is a bottleneck. */
    int lo = 0, hi = 255;
    while (lo < hi) {
        int mid = (lo + hi + 1) / 2;
        if (dl_srgb_to_linear_lut[mid] <= v) lo = mid;
        else hi = mid - 1;
    }
    return (unsigned char)lo;
}

#else  /* !DL_USE_LUT -- default: exact powf path */

static inline float linearRGB_from_sRGB(unsigned char v)
{
    float fv = v / 255.f;
    if (fv < 0.04045f) return fv / 12.92f;
    return powf((fv + 0.055f) / 1.055f, 2.4f);
}

static inline unsigned char sRGB_from_linearRGB(float v)
{
    if (v <= 0.f) return 0;
    if (v >= 1.f) return 255;
    if (v < 0.0031308f) return 0.5f + (v * 12.92 * 255.f);
    return 0.f + 255.f * (powf(v, 1.f / 2.4f) * 1.055f - 0.055f);
}

